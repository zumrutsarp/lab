cm = confusion_matrix(y_test, y_pred)

fig, ax = plt.subplots(figsize=(12, 10))
fig.patch.set_facecolor('#FAFAFA')
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
            xticklabels=class_names, yticklabels=class_names,
            linewidths=0.5, linecolor='white', ax=ax,
            cbar_kws={'shrink': 0.8})
ax.set_xlabel('Tahmin Edilen', fontsize=12, fontweight='bold', labelpad=10)
ax.set_ylabel('Gerçek', fontsize=12, fontweight='bold', labelpad=10)
ax.set_title('Confusion Matrix — Fashion-MNIST', fontsize=14, fontweight='bold', pad=15)
plt.xticks(rotation=45, ha='right', fontsize=10)
plt.yticks(rotation=0, fontsize=10)
plt.tight_layout()
plt.savefig('uygulama4_confusion_matrix.png', dpi=150, bbox_inches='tight')
plt.show()

# === 3. GRAFİK: En çok karıştırılan 3 sınıf ===
np.fill_diagonal(cm, 0)
errors = []
for i in range(10):
    for j in range(10):
        if i != j and cm[i,j] > 0:
            errors.append((cm[i,j], class_names[i], class_names[j]))
errors.sort(reverse=True)
top3 = errors[:3]

fig, ax = plt.subplots(figsize=(8, 5))
fig.patch.set_facecolor('#FAFAFA')
ax.set_facecolor('#F7F7F7')
for spine in ax.spines.values():
    spine.set_visible(False)

labels = [f'{e[1]}\n→ {e[2]}' for e in top3]

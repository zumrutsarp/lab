import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from tensorflow.keras import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, Flatten, Dense
from tensorflow.keras.datasets import fashion_mnist
from sklearn.metrics import confusion_matrix

# === VERİ HAZIRLA ===
class_names = ['T-shirt','Trouser','Pullover','Dress','Coat',
                'Sandal','Shirt','Sneaker','Bag','Ankle boot']

(x_train, y_train), (x_test, y_test) = fashion_mnist.load_data()
x_train = x_train.reshape(-1, 28, 28, 1) / 255.0
x_test  = x_test.reshape(-1, 28, 28, 1) / 255.0

# === MODELİ KUR ===
model = Sequential([
    Conv2D(32, (3,3), activation='relu', input_shape=(28,28,1)),
    MaxPooling2D(2,2),
    Conv2D(64, (3,3), activation='relu'),
    Flatten(),
    Dense(128, activation='relu'),
    Dense(10, activation='softmax')
])

model.compile(optimizer='adam',
              loss='sparse_categorical_crossentropy',
              metrics=['accuracy'])
model.summary()

# === EĞİT ===
history = model.fit(x_train, y_train, epochs=10, batch_size=64,
                    validation_data=(x_test, y_test), verbose=1)

# === 1. GRAFİK: Accuracy & Loss ===
fig, axes = plt.subplots(1, 2, figsize=(14, 5))
fig.patch.set_facecolor('#FAFAFA')
epochs_range = range(1, 11)

for ax in axes:
    ax.set_facecolor('#F7F7F7')
    ax.grid(True, color='white', linewidth=1.5)
    for spine in ax.spines.values():
        spine.set_visible(False)

axes[0].plot(epochs_range, history.history['accuracy'],     color='#378ADD', marker='o', linewidth=2.5, label='Train')
axes[0].plot(epochs_range, history.history['val_accuracy'], color='#378ADD', marker='s', linewidth=2.5, linestyle='--', label='Validation')
axes[0].fill_between(epochs_range, history.history['accuracy'], history.history['val_accuracy'], alpha=0.08, color='#378ADD')
axes[0].set_title('Model Accuracy', fontsize=13, fontweight='bold')
axes[0].set_ylabel('Accuracy'); axes[0].set_xlabel('Epoch')
axes[0].yaxis.set_major_formatter(plt.FuncFormatter(lambda v,_: f'{v*100:.1f}%'))
axes[0].legend()

axes[1].plot(epochs_range, history.history['loss'],     color='#D85A30', marker='o', linewidth=2.5, label='Train')
axes[1].plot(epochs_range, history.history['val_loss'], color='#D85A30', marker='s', linewidth=2.5, linestyle='--', label='Validation')
axes[1].fill_between(epochs_range, history.history['loss'], history.history['val_loss'], alpha=0.08, color='#D85A30')
axes[1].set_title('Model Loss', fontsize=13, fontweight='bold')
axes[1].set_ylabel('Loss'); axes[1].set_xlabel('Epoch')
axes[1].legend()

plt.suptitle('Fashion-MNIST CNN — Eğitim Süreci', fontsize=14, fontweight='bold', y=1.02)
plt.tight_layout()
plt.savefig('uygulama4_accuracy_loss.png', dpi=150, bbox_inches='tight')
plt.show()

# === 2. GRAFİK: Confusion Matrix ===

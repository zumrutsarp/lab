import tensorflow as tf
from tensorflow.keras import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, Flatten, Dense
from tensorflow.keras.datasets import fashion_mnist
import time

# Veriyi hazırla
(x_train, y_train), (x_test, y_test) = fashion_mnist.load_data()
x_train = x_train.reshape(-1, 28, 28, 1) / 255.0
x_test = x_test.reshape(-1, 28, 28, 1) / 255.0

# --- MODEL 1: MaxPooling kullanan model ---
model_pool = Sequential([
    Conv2D(32, (3,3), activation='relu', padding='same', input_shape=(28,28,1)),
    MaxPooling2D(pool_size=(2,2)),
    Conv2D(64, (3,3), activation='relu', padding='same'),
    MaxPooling2D(pool_size=(2,2)),
    Flatten(),
    Dense(128, activation='relu'),
    Dense(10, activation='softmax')
], name="MaxPooling_Model")

# --- MODEL 2: Stride=2 kullanan model ---
model_stride = Sequential([
    Conv2D(32, (3,3), activation='relu', padding='same', strides=2, input_shape=(28,28,1)),
    Conv2D(64, (3,3), activation='relu', padding='same', strides=2),
    Flatten(),
    Dense(128, activation='relu'),
    Dense(10, activation='softmax')
], name="Stride2_Model")

# Her iki modeli derle
model_pool.compile(optimizer='adam', loss='sparse_categorical_crossentropy', metrics=['accuracy'])
model_stride.compile(optimizer='adam', loss='sparse_categorical_crossentropy', metrics=['accuracy'])

# Özetleri yazdır
print("=== MODEL 1: MaxPooling ===")
model_pool.summary()

print("\n=== MODEL 2: Stride=2 ===")
model_stride.summary()

# Eğit
print("\nModel 1 eğitiliyor...")
h1 = model_pool.fit(x_train, y_train, epochs=5, batch_size=64,
                     validation_data=(x_test, y_test), verbose=1)

print("\nModel 2 eğitiliyor...")
h2 = model_stride.fit(x_train, y_train, epochs=5, batch_size=64,
                       validation_data=(x_test, y_test), verbose=1)

# Sonuçları karşılaştır
pool_acc = max(h1.history['val_accuracy'])
stride_acc = max(h2.history['val_accuracy'])

print("\n========== SONUÇLAR ==========")
print(f"MaxPooling Modeli  - En iyi val_accuracy: {pool_acc:.4f}")
print(f"Stride=2 Modeli    - En iyi val_accuracy: {stride_acc:.4f}")

